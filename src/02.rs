fn main() {
    println!("Part 1 is {}", part1(input()));
    println!("Part 2 is {}", part2(input()));
}

enum Shape {
    Rock,
    Paper,
    Scissors,
}

fn part1(input: &'static str) -> u32 {
    input
        .trim()
        .lines()
        .map(|line| {
            let (oponent, me) = line.trim().split_once(' ').unwrap();

            let oponent = match oponent {
                "A" => Shape::Rock,
                "B" => Shape::Paper,
                "C" => Shape::Scissors,
                _ => panic!(),
            };
            let me = match me {
                "X" => Shape::Rock,
                "Y" => Shape::Paper,
                "Z" => Shape::Scissors,
                _ => panic!(),
            };

            let shape_score = match me {
                Shape::Rock => 1,
                Shape::Paper => 2,
                Shape::Scissors => 3,
            };

            let game_score = match (me, oponent) {
                (Shape::Rock, Shape::Scissors) => 6,
                (Shape::Scissors, Shape::Paper) => 6,
                (Shape::Paper, Shape::Rock) => 6,
                (Shape::Scissors, Shape::Rock) => 0,
                (Shape::Rock, Shape::Paper) => 0,
                (Shape::Paper, Shape::Scissors) => 0,
                (Shape::Scissors, Shape::Scissors) => 3,
                (Shape::Rock, Shape::Rock) => 3,
                (Shape::Paper, Shape::Paper) => 3,
            };

            shape_score + game_score
        })
        .sum()
}

fn part2(input: &'static str) -> u32 {
    input
        .trim()
        .lines()
        .map(|line| {
            let (oponent, result) = line.trim().split_once(' ').unwrap();

            let oponent = match oponent {
                "A" => Shape::Rock,
                "B" => Shape::Paper,
                "C" => Shape::Scissors,
                _ => panic!(),
            };
            let me = match result {
                "X" => match oponent {
                    Shape::Rock => Shape::Scissors,
                    Shape::Paper => Shape::Rock,
                    Shape::Scissors => Shape::Paper,
                },
                "Y" => oponent,
                "Z" => match oponent {
                    Shape::Rock => Shape::Paper,
                    Shape::Paper => Shape::Scissors,
                    Shape::Scissors => Shape::Rock,
                },
                _ => panic!(),
            };

            let shape_score = match me {
                Shape::Rock => 1,
                Shape::Paper => 2,
                Shape::Scissors => 3,
            };

            let game_score = match result {
                "X" => 0,
                "Y" => 3,
                "Z" => 6,
                _ => panic!(),
            };

            shape_score + game_score
        })
        .sum()
}

